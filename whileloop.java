public class whileloop {
    public static void main(String[] args){
        int i=1;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        System.out.println("-----------------------------------------------------");
        int j=10;
        while(j>=1){
            System.out.println(j);
            j--;
        }
        System.out.println("-----------------------------------------------------");
        int k=0;
        while(k<=10){
            System.out.println(k);
            k=k+2;
        }
        System.out.println("------------------------------------------------------");
        int s=10;
        while(s>=0){
            System.out.println(s);
            s=s-2;
        }


    }
    
}
