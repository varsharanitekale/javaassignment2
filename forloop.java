public class forloop {
public static void main(String[] args){
    for(int j=0; j<=10; j=j+2){
        System.out.println(j);
    }
    System.out.println("------------------------------------\n------------------------------------");
    for( int i=10 ; i>=0; i=i-2){
        System.out.println(i);
    }
}
    
}
